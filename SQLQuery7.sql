CREATE PROCEDURE CLIENTES_POR_CIDADE_IDADE
    @CIDADE VARCHAR(30),
    @IDADE_MAX INT
AS
BEGIN
    SELECT 
        NOME AS [Nome do Cliente],
        DATANASCIMENTO AS [Data Nascimento],
        DATEDIFF(YEAR, DATANASCIMENTO, GETDATE()) 
- CASE 
    WHEN DATEADD(YEAR, DATEDIFF(YEAR, DATANASCIMENTO, GETDATE()), DATANASCIMENTO) > GETDATE() 
    THEN 1 ELSE 0
  END as [idade]
    FROM CLIENTES
    WHERE 
        CIDADE = @CIDADE
        AND DATEDIFF(YEAR, DATANASCIMENTO, GETDATE()) <= @IDADE_MAX
END

