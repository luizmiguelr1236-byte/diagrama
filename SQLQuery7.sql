CREATE PROCEDURE DEVOLVER_FILME
    @ID_LOCACAO NUMERIC(18,0)
AS
BEGIN
    DECLARE @ID_FILME NUMERIC(18,0)

    SELECT @ID_FILME = COD_FILME
    FROM LOCACOES
    WHERE COD_LOCACAO = @ID_LOCACAO

    UPDATE LOCACOES
    SET DATA_DEVOLUCAO = GETDATE()
    WHERE COD_LOCACAO = @ID_LOCACAO

    UPDATE FILME
    SET STATUS = 'Disponivel'
    WHERE COD_FILME = @ID_FILME

	select COD_FILME, FILME, VALOR_LOCACAO, STATUS from FILME
END

