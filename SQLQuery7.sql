CREATE PROCEDURE Aniversariantes (@MES INT)
AS
BEGIN
    IF EXISTS (
        SELECT 1 
        FROM CLIENTES 
        WHERE MONTH(DATANASCIMENTO) = @MES
    )
    BEGIN
        SELECT 
            NOME,
            DAY(DATANASCIMENTO) AS DIA_ANIVERSARIO
        FROM CLIENTES
        WHERE MONTH(DATANASCIMENTO) = @MES
        ORDER BY DAY(DATANASCIMENTO)
    END
    ELSE
    BEGIN
        PRINT 'Sem aniversariantes neste męs.'
    END
END

execute Aniversariantes 11

