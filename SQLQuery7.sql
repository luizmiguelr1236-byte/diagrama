CREATE PROCEDURE incluir_clientes
    @RG VARCHAR(9),
    @NOME VARCHAR(50),
    @ENDERECO VARCHAR(50),
    @BAIRRO VARCHAR(30),
    @CIDADE VARCHAR(30), 
    @ESTADO CHAR(2),
    @TELEFONE VARCHAR(15),
    @EMAIL VARCHAR(30),
    @DATANASCIMENTO DATETIME,
    @Sexo CHAR(1)
AS
BEGIN
    INSERT INTO dbo.CLIENTES
    (
        RG, NOME, ENDERECO, BAIRRO, CIDADE, ESTADO,
        TELEFONE, EMAIL, DATANASCIMENTO, Sexo
    )
    VALUES
    (
        @RG, @NOME, @ENDERECO, @BAIRRO, @CIDADE, @ESTADO,
        @TELEFONE, @EMAIL, @DATANASCIMENTO, @Sexo
    )
END
GO

create procedure Selecionar_clientes (@ID float)
as
begin 
