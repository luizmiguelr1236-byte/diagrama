CREATE PROCEDURE INCLUIR_LOCACAO
    @ID_CLIENTE NUMERIC(18,0),
    @ID_FILME NUMERIC(18,0)
AS
BEGIN
    DECLARE @DATA_LOCACAO DATETIME = GETDATE()

    INSERT INTO LOCACOES (COD_CLIENTE, COD_FILME, DATA_LOCACAO)
    VALUES (@ID_CLIENTE, @ID_FILME, @DATA_LOCACAO)

    UPDATE FILME
    SET STATUS = 'Alugado', RESERVADA = 's'
    WHERE COD_FILME = @ID_FILME

	select STATUS from FILME 
END

execute INCLUIR_LOCACAO 6, 7

go

